<h2 class="wp-block-heading" id="quieres-conocer-a-las-ballenas-francas-australes">¿Quieres conocer a las ballenas francas australes?</h2>
<!-- /wp:heading -->

<!-- wp:paragraph -->
<p>¡No te pierdas la oportunidad de vivir una experiencia única e inolvidable! Con 40 toneladas de peso y 15 metros de largo, estas majestuosas criaturas marinas nadan cerca del continente y son curiosas, lo que las convierte en las protagonistas de un espectáculo natural increíble que se puede disfrutar de junio a diciembre en Chubut.</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p>Esta área natural protegida es uno de los pocos lugares del mundo donde estos gigantes mamíferos acuden anualmente para aparearse, dar a luz, amamantar y socializar. A través de las excursiones de avistamiento embarcado, ofrecidas por seis empresas desde Puerto Pirámides, se puede disfrutar del mejor avistaje del mundo durante dos horas.</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p>Sin embargo, si prefieres una vista costera, la reserva natural El Doradillo, a 15 kilómetros al norte de Puerto Madryn, también ofrece la oportunidad de ver a las ballenas a muy pocos metros de la costa desde junio hasta octubre. En esta playa profunda, las ballenas se divierten saltando, provocando explosiones de espuma de mar o asomándose de manera vertical, en la posición "de espionaje". ¡Un espectáculo natural impresionante!</p>
